package kr.co.rocketkurly.cust.vo;

import java.sql.Date;

public class QuestionVO {

	private String member_id, title, type, contents, answer;		
	private int question_no, order_no;
	private Date reg_dt;
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	public int getQuestion_no() {
		return question_no;
	}
	public void setQuestion_no(int question_no) {
		this.question_no = question_no;
	}
	public int getOrder_no() {
		return order_no;
	}
	public void setOrder_no(int order_no) {
		this.order_no = order_no;
	}
	public Date getReg_dt() {
		return reg_dt;
	}
	public void setReg_dt(Date reg_dt) {
		this.reg_dt = reg_dt;
	}
	@Override
	public String toString() {
		return "QuestionVO [member_id=" + member_id + ", title=" + title + ", type=" + type + ", contents=" + contents
				+ ", answer=" + answer + ", question_no=" + question_no + ", order_no=" + order_no + ", reg_dt="
				+ reg_dt + "]";
	}
	
	
	
}
